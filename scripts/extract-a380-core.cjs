const fs = require('node:fs');
const path = require('node:path');

const root = process.cwd();

const referencePath = path.join(
  root,
  'src',
  'performance',
  'aircraft',
  'a380',
  'a380-takeoff-performance.reference.txt',
);

const outputPath = path.join(
  root,
  'src',
  'performance',
  'aircraft',
  'a380',
  'a380Core.ts',
);

if (!fs.existsSync(referencePath)) {
  throw new Error(`Reference file not found: ${referencePath}`);
}

const source = fs.readFileSync(referencePath, 'utf8');

const start = source.indexOf('type RunwayCondition');
const end = source.indexOf('// AIRPORT / RUNWAY LOOKUP');

if (start === -1 || end === -1) {
  throw new Error('Could not find calculator section in the reference file.');
}

let core = source.slice(start, end);

// Remove airport/METAR-only interfaces from the extracted calculator core.
core = core.replace(
  /interface AirportRecord[\s\S]*?interface A380WeightVariant/,
  'interface A380WeightVariant',
);

// Remove CLI/network constants that belong in airportWeatherApi.ts now.
core = core.replace(/const DATA_URLS = \{[\s\S]*?\n\};\s*\n/, '');

// Remove currently-unused loading data so strict TypeScript does not fail.
core = core.replace(/const A380X_LOADING = \{[\s\S]*?\n\};\s*\n/, '');

// Export the weight variant and calculator function.
core = core.replace(
  'const DEFAULT_WEIGHT_VARIANT: A380WeightVariant =',
  'export const DEFAULT_WEIGHT_VARIANT: A380WeightVariant =',
);

core = core.replace(
  'function calculateTakeoffPerformance(input: PilotInput): TakeoffResult',
  'export function calculateTakeoffPerformance(input: PilotInput): TakeoffResult',
);

// Avoid strict unused-parameter errors.
core = core.replace(/densityAltitudeFt: number/g, '_densityAltitudeFt: number');

// The original helper functions were below the CLI section.
// Add only the pure helpers needed by the calculator.
const helpers = `
function calculatePressureAltitude(elevationFt: number, qnhHpa: number): number {
  return elevationFt + (1013.25 - qnhHpa) * 27;
}

function calculateWindComponents(
  windDirectionDeg: number,
  windSpeedKt: number,
  runwayHeadingDeg: number,
): { headwindKt: number; crosswindKt: number } {
  const angleRad = ((windDirectionDeg - runwayHeadingDeg) * Math.PI) / 180;

  return {
    headwindKt: windSpeedKt * Math.cos(angleRad),
    crosswindKt: windSpeedKt * Math.sin(angleRad),
  };
}

function clamp(value: number, min: number, max: number): number {
  return Math.max(min, Math.min(max, value));
}

function round1(value: number): number {
  return Math.round(value * 10) / 10;
}

function round3(value: number): number {
  return Math.round(value * 1000) / 1000;
}
`;

const finalFile = `// Auto-extracted from a380-takeoff-performance.reference.txt
// Pure browser-safe calculator core.
// Do not edit this file manually unless the extractor is no longer used.
// SIMULATION TOOL ONLY. NOT FOR REAL WORLD AVIATION.

${core}

${helpers}
`;

fs.writeFileSync(outputPath, finalFile, 'utf8');

console.log(`Created ${outputPath}`);